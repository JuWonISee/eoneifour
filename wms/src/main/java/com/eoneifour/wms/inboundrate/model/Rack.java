package com.eoneifour.wms.inboundrate.model;

public class Rack {
	private int rack_id;
	private int s;
	private int z;
	private int x;
	private int y;
	private int rack_status;
	
	public int getRack_id() {
		return rack_id;
	}
	public void setRack_id(int rack_id) {
		this.rack_id = rack_id;
	}
	public int getS() {
		return s;
	}
	public void setS(int s) {
		this.s = s;
	}
	public int getZ() {
		return z;
	}
	public void setZ(int z) {
		this.z = z;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getRack_status() {
		return rack_status;
	}
	public void setRack_status(int rack_status) {
		this.rack_status = rack_status;
	}
	
	
}
