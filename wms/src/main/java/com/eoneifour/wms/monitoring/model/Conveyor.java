package com.eoneifour.wms.monitoring.model;

public class Conveyor {
	private int conveyor_id;
	private int s;
	private int z;
	private int x;
	private int y;
	private int on_product;
	
	public int getConveyor_id() {
		return conveyor_id;
	}
	public void setConveyor_id(int conveyor_id) {
		this.conveyor_id = conveyor_id;
	}
	public int getS() {
		return s;
	}
	public void setS(int s) {
		this.s = s;
	}
	public int getZ() {
		return z;
	}
	public void setZ(int z) {
		this.z = z;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getOn_product() {
		return on_product;
	}
	public void setOn_product(int on_product) {
		this.on_product = on_product;
	}
	
	
}
